package com.dassa.vo;

public class MoveAddrScheduleVO {

	private String date;
	private String time;
	private String moveHope;
	private String moveHelp;

	public String getDate() {
		return date;
	}

	public void setDate(String date) {
		this.date = date;
	}

	public String getTime() {
		return time;
	}

	public void setTime(String time) {
		this.time = time;
	}

	public String getMoveHope() {
		return moveHope;
	}

	public void setMoveHope(String moveHope) {
		this.moveHope = moveHope;
	}

	public String getMoveHelp() {
		return moveHelp;
	}

	public void setMoveHelp(String moveHelp) {
		this.moveHelp = moveHelp;
	}
}
